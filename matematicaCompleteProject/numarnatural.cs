using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace matematicaCompleteProject
{
    public class numarnatural
    {
        public int valoare;

        public int adunare(int b)
        {
            return valoare + b;
        }

        public int scadere(int b)
        {
            return valoare - b;
        }

        public int inmultire(int b)
        {
            return valoare * b;
        }

        public int impartire(int b)
        {
            return valoare / b;
        }

        

    }
}
